    private void brnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnVerificarActionPerformed
        // TODO add your handling code here:
        int a = sliA.getValue();
        int b = sliB.getValue();
        int c = sliC.getValue();
        
        if(a<b+c && b<a+c && c<a+b){
            lblStatus.setText("Formam um tri√¢ngulo");
            if (a==b && b==c){
                lblTipo.setText("√â um tri√¢ngulo equil√°tero");
            }else if (a!=b && b!=c && a!=c){
                lblTipo.setText("√â um trin√¢ngulo escaleno");
            }else{
                lblTipo.setText("√â um tri√¢ngulo is√≥celes");
            }
        }else{
            lblStatus.setText ("N√£o formam um tri√¢ngulo");
            lblTipo.setText("-");
        }
        
        panResp.setVisible(true);
    }//GEN-LAST:event_brnVerificarActionPerformed

